function createVideosElements(videos) {
  const ulContainer = document.getElementById("videos__container");
  const li = document.createElement("li");
  li.classList.add("videos__item");

  videos.map((videos) => {
    li.innerHTML = `
            <iframe  width="100%"  height="72%" src="${videos.src}" 
            title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" allowfullscreen></iframe>
            <div class="descripcion-video">
                <img src="${videos.logo}" alt="${videos.canal}">
                <div class="detalles__video">
                    <h3>${videos.titulo}</h3>
                    <p>${videos.vistas}</p>
                </div>
            </div>
        
`;
  });

  ulContainer.appendChild(li);
}

